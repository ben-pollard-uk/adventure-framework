namespace BP.AdventureFramework.Structure
{
    /// <summary>
    /// Enumeration of game commands.
    /// </summary>
    public enum GameCommand
    {
        /// <summary>
        /// New game.
        /// </summary>
        New = 0,
        /// <summary>
        /// Exit the game.
        /// </summary>
        Exit,
        /// <summary>
        /// View help.
        /// </summary>
        Help,
        /// <summary>
        /// View the region map.
        /// </summary>
        Map,
        /// <summary>
        /// View information about the game.
        /// </summary>
        About
    }
}