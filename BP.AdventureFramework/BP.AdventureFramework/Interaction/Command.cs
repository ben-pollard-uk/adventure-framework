namespace BP.AdventureFramework.Interaction
{
    /// <summary>
    /// Enumeration of commands.
    /// </summary>
    public enum Command
    {
        /// <summary>
        /// Use an item.
        /// </summary>
        Use = 0,
        /// <summary>
        /// Take an item.
        /// </summary>
        Take,
        /// <summary>
        /// Examine something.
        /// </summary>
        Examine,
        /// <summary>
        /// Drop an item.
        /// </summary>
        Drop,
        /// <summary>
        /// Talk to a non-playable character.
        /// </summary>
        Talk
    }
}