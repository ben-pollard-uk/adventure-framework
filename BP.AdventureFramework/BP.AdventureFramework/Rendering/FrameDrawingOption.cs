namespace BP.AdventureFramework.Rendering
{
    /// <summary>
    /// Enumeration of frame drawing options.
    /// </summary>
    public enum FrameDrawingOption
    {
        /// <summary>
        /// Turn the key on.
        /// </summary>
        KeyOn = 0,
        /// <summary>
        /// Turn the key off.
        /// </summary>
        KeyOff,
        /// <summary>
        /// Turn commands on.
        /// </summary>
        CommandsOn,
        /// <summary>
        /// Turn commands off.
        /// </summary>
        CommandsOff,
        /// <summary>
        /// Invert display colours.
        /// </summary>
        Invert
    }
}